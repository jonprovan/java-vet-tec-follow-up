package com.skillstorm;

public class AccessModifiers {
	
	// ACCESS MODIFIERS IN JAVA
	
	/*
	 * instance and static variables and methods CAN have access modifiers
	 * local variables in methods, loops, etc. CANNOT have access modifiers
	 * 
	 * these modifiers indicate what level of access other classes have to these variables/methods
	 * 
	 * we can also apply them to classes in general, like the public modifier on this class
	 * 
	 * public					accessible in any class in the project
	 * protected				accessible in the same package AND a subclass in a different package
	 * default (no modifier)	accessible ONLY in the same package
	 * private					accessible ONLY in the same class
	 * 
	 * -- Look at w18-d1-exercises AnimalTest.java for an example of the limitations of "protected"
	 * 
	 */

}
